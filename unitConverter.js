function fromDollarToYen(amount) {
    // Conversion rate: 1 USD = 110 Yen
    return amount * 110;
}

function fromEuroToDollar(amount) {
    // Conversion rate: 1 Euro = 1.17 USD
    return amount * 1.17;
}

function fromYenToPound(amount) {
    // Conversion rate: 1 Yen = 0.0067 GBP
    return amount * 0.0067;
}
